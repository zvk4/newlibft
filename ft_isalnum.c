int	ft_isalnum(int c)
{
    if ((c >= 97 && c <= 122) || (c >= 65 && c <= 90)
    || (c >= 48 && c <= 57))
         return (1);
    return (0);
}

#include <stdio.h>
int main(){
    int a = '.';
    printf("%d", ft_isalnum(a));
}