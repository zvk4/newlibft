#include "libft.h"

size_t  ft_strlcat(char *dst, const char *src, size_t size)
{
    size_t  i;
    size_t  dst_len;
    size_t  src_len;

    dst_len = 0;
    src_len = 0;
    i = 0;
    while (dst[dst_len] && dst_len < size)
        dst_len++;
    while(src[src_len])
        src_len++;
    if(dst_len >= size)
        return (src_len + size);
    while (src[i] && (dst_len + i + 1) < size)
    {
        dst[dst_len + i] = src[i];
        i++;
    }
    if (dst_len + i < size)
        dst[dst_len + i] = '\0';
    return (dst_len + src_len);
}

